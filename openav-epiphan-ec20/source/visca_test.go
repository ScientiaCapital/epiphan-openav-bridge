package main

import (
	"bytes"
	"testing"
)

func TestVISCAEncoders(t *testing.T) {
	cases := []struct {
		name string
		got  []byte
		want []byte
	}{
		{"preset recall 5", viscaPresetRecall(5),
			[]byte{0x81, 0x01, 0x04, 0x3F, 0x02, 0x05, 0xFF}},
		{"preset recall 0", viscaPresetRecall(0),
			[]byte{0x81, 0x01, 0x04, 0x3F, 0x02, 0x00, 0xFF}},
		{"preset set 12", viscaPresetSet(12),
			[]byte{0x81, 0x01, 0x04, 0x3F, 0x01, 0x0C, 0xFF}},
		{"home", viscaHome(),
			[]byte{0x81, 0x01, 0x06, 0x04, 0xFF}},
		{"jog left, tilt stop", viscaJog(viscaPanLeft, viscaTiltStop, 0x10, 0x0E),
			[]byte{0x81, 0x01, 0x06, 0x01, 0x10, 0x0E, 0x01, 0x03, 0xFF}},
		{"jog up-right", viscaJog(viscaPanRight, viscaTiltUp, 0x08, 0x08),
			[]byte{0x81, 0x01, 0x06, 0x01, 0x08, 0x08, 0x02, 0x01, 0xFF}},
		{"stop", viscaStop(0x10, 0x0E),
			[]byte{0x81, 0x01, 0x06, 0x01, 0x10, 0x0E, 0x03, 0x03, 0xFF}},
	}
	for _, c := range cases {
		if !bytes.Equal(c.got, c.want) {
			t.Errorf("%s = % X, want % X", c.name, c.got, c.want)
		}
	}
}

func TestVISCAWrap(t *testing.T) {
	payload := []byte{0x81, 0x01, 0x06, 0x04, 0xFF} // home, 5 bytes
	got := viscaWrap(viscaTypeCommand, 1, payload)
	// header: type 0x0100, len 0x0005, seq 0x00000001, then payload
	want := []byte{0x01, 0x00, 0x00, 0x05, 0x00, 0x00, 0x00, 0x01,
		0x81, 0x01, 0x06, 0x04, 0xFF}
	if !bytes.Equal(got, want) {
		t.Errorf("viscaWrap = % X, want % X", got, want)
	}
}

func TestVISCAWrap_InquirySeq(t *testing.T) {
	payload := []byte{0x81, 0x09, 0x06, 0x12, 0xFF} // pan/tilt inquiry, 5 bytes
	got := viscaWrap(viscaTypeInquiry, 0x0A, payload)
	want := []byte{0x01, 0x10, 0x00, 0x05, 0x00, 0x00, 0x00, 0x0A,
		0x81, 0x09, 0x06, 0x12, 0xFF}
	if !bytes.Equal(got, want) {
		t.Errorf("viscaWrap inquiry = % X, want % X", got, want)
	}
}
